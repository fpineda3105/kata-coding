function smallestSum(array) {    
    array = array.sort((a,b) => a - b)
    let min = array[0]
    nextIndex = 1    
    while (nextIndex < array.length) {
        if (array[nextIndex] === 1 || min === 1) {
          return array.length
        }
        if (array[nextIndex] % min === 0) {
          array[nextIndex] = min
          nextIndex++
          continue            
        }
        array[nextIndex] = array[nextIndex] - min
        if (array[nextIndex] === min) {
          nextIndex++
          continue
        }
        if (array[nextIndex] < min) {
          let tempMin = array[nextIndex]
          array[nextIndex] = min
          min = tempMin
        }
        
    }
    return min * array.length   

}

